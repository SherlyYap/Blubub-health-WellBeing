      } else {
        _finishTest();
      }
    });
  }

Future<void> _finishTest() async {
  final now = DateTime.now().toIso8601String(); // ✅ format ISO
  await TestHistoryDB.insertResult(widget.testType, score, now);

  showDialog(
    context: context,
    builder: (ctx) => AlertDialog(
      title: const Text("Tes Selesai"),
      content: Text(
        "Skor kamu: $score dari ${questions.length}\n\nHasil ini disimpan ke riwayat tes.",
        style: GoogleFonts.nunito(fontSize: 16),
      ),
      actions: [
        TextButton(
          onPressed: () {
            Navigator.pop(ctx);
            Navigator.pop(context);
          },
          child: const Text("OK"),
        ),
      ],
    ),
  );
}


